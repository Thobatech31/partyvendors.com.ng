

$('#toggle_message3').click(function(){
	var value = $('#toggle_message3').attr('value');
	$('#message3').toggle('slow');
	
	if(value == 'DEVELOPER'){
		$('#toggle_message3').attr('value','DEVELOPER');	
	}
			else if(value == 'Show'){
		$('#toggle_message3').attr('value','Hide');	
		}	
});

